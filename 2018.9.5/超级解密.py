import re

#   #   问题分析:
#   每一个密码为小写字母
#   每个密码左右二边均只有3个大写字母
#   #   分析解决
#   我打算用正则表达式
#   首先找到并以列表的形式记录所有小写字母的位置
#                   利用for循环
#   然后逐一验证小写字母左右二边是否为大写字母
#                   先用截片前后3并合并成字符串
#                   判断是否全部为大写字母
#                           否:  则不加入结果列表
#   循环验证后,输出所有列表对应元素
def supercount( content ):
    az = 'qwertyuiopasdfghjklzxcvbnm'
    password = [] # 小写字母存储列表
    result = [] # 结果列表
    posnum = 0

    for i in content:   #   记录所有小写字母位置
        posnum += 1
        for y in az:
            if i == y:
                password.append(posnum-1)

    for each in password: # 截取截片并进行比较
        setstr = content[each-3:each] + content[each+1:each+4]
        partten = re.compile('[A-Z]') # 如果全部为大写字母则加入结果列表
        if len(partten.findall(setstr)) == 6:
            result.append(each)

    for end in result: # 输出结果
        print(content[end],end='')


